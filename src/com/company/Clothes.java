package com.company;

public class Clothes {
    private String Color;
    private String Size;

    public Clothes() {
    }

    public Clothes(String color, String size) {
        Color = color;
        Size = size;
    }

    public String getColor() {
        return Color;
    }

    public void setColor(String color) {
        Color = color;
    }

    public String getSize() {
        return Size;
    }

    public void setSize(String size) {
        Size = size;
    }


}
